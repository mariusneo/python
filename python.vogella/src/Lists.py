mylist = ["Linux", "Mac OS", "Windows"]
# Print the first element from the list
print(mylist[0])
# Print the last element from the list
# Negative values start from the end of the list
print(mylist[-1])
# sublists
print(mylist[0:2])
# add elements to the list
mylist.append("Android")
# print the content of the list
for element in mylist:
    print(element)
